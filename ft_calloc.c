#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ret;

	ret = malloc(size * count);
	if (!ret)
		return (0);
	if (count == SIZE_MAX || size == SIZE_MAX)
		return (NULL);
	ft_bzero(ret, size * count);
	return (ret);
}
